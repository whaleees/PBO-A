public class Manusia extends MakhlukHidup {
    @Override
    public void makan() {
        System.out.println("Manusia makan dengan tangan menggunakan alat seperti sendok atau garpu.");
    }
    @Override
    public void bernafas() {
        System.out.println("Manusia bernafas menggunakan paru-paru.");
    }
    @Override
    public void beraktivitas() {
        System.out.println("Manusia beraktivitas dengan bekerja, belajar, atau bersosialisasi.");
    }
}